function love.conf(t)
    t.identity = "Pocket Watch"                    -- The name of the save directory (string)
 
    t.window.title = "Pocket Watch"         -- The window title (string)
    t.window.icon = "/assets/icon.png"                 -- Filepath to an image to use as the window's icon (string)
    t.window.width = 1024                -- The window width (number)
    t.window.height = 624               -- The window height (number)
end